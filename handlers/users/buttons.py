@dp.message_handler(IsPrivate(),
                    text=['üìç–ú—É—Å–æ—Ä–Ω—ã–µ –±–∞–∫–∏', 'üìç–ü–µ—Ä–µ—Ä–∞–±–æ—Ç–∫–∞ –º—É—Å–æ—Ä–∞',
                          'üìç–í—ã–≤–æ–∑ –º—É—Å–æ—Ä–∞'])
async def find_obj(message: types.Message, state: FSMContext):
    answer = message.text

    await message.answer(f"–í—ã–±—Ä–∞–Ω —Ä–∞–∑–¥–µ–ª {answer}. –ß—Ç–æ –±—É–¥–µ–º –¥–µ–ª–∞—Ç—å?",
                         reply_markup=kb_send_location)
    await state.update_data(type=answer)
    await GetGeo.type.set()


@dp.message_handler(IsPrivate(),
                    content_types=['location'],
                    state=GetGeo.type)
async def get_geo(message: types.Message, state: FSMContext):
    latitude = message.location.latitude
    longitude = message.location.longitude
    data = await state.get_data()
    ttype = data.get("type")
    ddata = find_smth(latitude, longitude, ttype)

    try:
        key, value = next(iter(ddata.items()))
        address_name = key if key else geocoder(value['lat'], value['lon'])
    except StopIteration:
        address_name = None

    if address_name:
        await message.answer("–ë–ª–∏–∂–∞–π—à–∏–π –Ω–∞–π–¥–µ–Ω–Ω—ã–π –æ–±—ä–µ–∫—Ç:")
        await dp.bot.send_venue(
            message.from_user.id,
            value['lat'],
            value['lon'],
            ttype,
            address_name
        )
    else:
        await message.answer(
            f"–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, –º—ã –Ω–µ —Å–º–æ–≥–ª–∏ –Ω–∞–π—Ç–∏ {ttype} –Ω–µ–ø–æ–¥–∞–ª—ë–∫—É(")
    await state.finish()
    await menu(message)


